# Function to add two numbers

def demo_add_two(a,b):
    '''Function to add two numbers
      call: demo_add_two(4,3)
      output: 4+3 = 7
    '''
    return a+b

# Function to subtract two numbers

def demo_sub_two(a,b):
    '''Function to subtract two numbers
       call: demo_sub_two(4,3)
       output: 4-3 = 1
    '''
    return a-b