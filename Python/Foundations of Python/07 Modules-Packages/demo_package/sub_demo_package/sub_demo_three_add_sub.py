# Functions to add/subtract two/two numbers

def demo_add_three(a,b,c):
    '''Function to add three numbers
       call function as demo_add_three(1,2,3)
       demo_add_three returns output as 1+2+3 = 6
    '''
    return a+b+c

# Function to subtract three numbers

def demo_sub_three(a,b,c):
    '''Function to subtract three numbers
       call function as demo_sub_three(1,2,3)
       demo_sub_three returns output as 1-2-3 = -4 
    '''
    
    return a-b-c